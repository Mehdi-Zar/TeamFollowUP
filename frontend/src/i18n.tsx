interface I18n {
  lang: Lang;
  setLang: (l: Lang) => void;
  t: (key: string, vars?: Record<string, string | number>) => string;
  rag: (s: Rag) => string;
  roadmap: (s: RoadmapStatus) => string;
  trend: (s: Trend) => string;
  role: (r: string) => string;
  freshness: (f: { age_days: number | null; never_submitted: boolean; is_stale?: boolean }) => string;
  formatDate: (iso?: string | null) => string;
  formatDateTime: (iso?: string | null) => string;
}

const I18nContext = createContext<I18n | undefined>(undefined);

/**
 * Provides the i18n context. Initialises the language from localStorage
 * (defaulting to French), persists it and mirrors it onto `<html lang>` on
 * change, and builds the translation/formatting functions for consumers.
 */
export function I18nProvider({ children }: { children: ReactNode }) {
  const [lang, setLangState] = useState<Lang>(() => (localStorage.getItem("trt_lang") as Lang) || "fr");
  useEffect(() => {
    localStorage.setItem("trt_lang", lang);
    document.documentElement.lang = lang;
  }, [lang]);

  /**
   * Translate `key` for the active language, interpolating any `{name}`
   * placeholders from `vars`. Falls back to the raw key if it's missing, so a
   * missing translation is visible rather than blank.
   */
  const t = (key: string, vars?: Record<string, string | number>) => {
    let s = DICT[lang][key] ?? key;
    if (vars) for (const k of Object.keys(vars)) s = s.replace(`{${k}}`, String(vars[k]));
    return s;
  };
  const locale = lang === "fr" ? "fr-FR" : "en-GB";
  const value: I18n = {
    lang,
    setLang: setLangState,
    t,
    rag: (s) => RAG[lang][s],
    roadmap: (s) => ROADMAP[lang][s],
    trend: (s) => TREND[lang][s],
    role: (r) => ROLE[lang][r] ?? r,
    freshness: (f) => {
      if (f.never_submitted || f.age_days === null) return t("fresh.never");
      if (f.age_days === 0) return t("fresh.today");
      if (f.age_days === 1) return t("fresh.one");
      return t("fresh.days", { n: f.age_days });
    },
    formatDate: (iso) => (iso ? new Date(iso).toLocaleDateString(locale, { day: "2-digit", month: "2-digit", year: "numeric" }) : "-"),
    formatDateTime: (iso) =>
      iso ? new Date(iso).toLocaleString(locale, { day: "2-digit", month: "2-digit", year: "numeric", hour: "2-digit", minute: "2-digit" }) : "-",
  };
  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
}

/**
 * Access the i18n context (`t`, `lang`, `setLang`, and the status/date
 * formatters). Throws if used outside {@link I18nProvider}.
 */
export function useI18n() {
  const ctx = useContext(I18nContext);
  if (!ctx) throw new Error("useI18n must be used within I18nProvider");
  return ctx;
}
